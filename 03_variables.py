# =====================================
# Python Data Types
# =====================================

# Integer (int)
print(type(1))

# Float
print(type(1.0))

# String (str)
print(type("1"))

# Boolean (bool)
print(type(True))

# Negative numbers
print(type(-10))
print(type(-10.0))


# =====================================
# Collection Data Types
# =====================================

# Tuple
print(type((1, 5, 3)))

# List
print(type([1, 5, 3, "S", False]))

# Dictionary
print(type({"name": "John", "age": 30}))

# Set
print(type({1, 2, 3, 0}))


# =====================================
# Special Data Types
# =====================================

# NoneType
print(type(None))

# Function type
print(type(print))

# Type object
print(type(type(1)))


# =====================================
# Comparison Operators
# =====================================

print(type(2 == 2))
print(2 == 2)


# =====================================
# Variables
# =====================================

name = "Sara"  # variable names are case-sensitive
print(name)

print("name")  # this is a string, not the variable


# =====================================
# Multi-line String
# =====================================

info = """
Name: Sara
Specialty: IT
"""

print(info)


# =====================================
# Variable Naming Conventions
# =====================================

# Single word
# name

# Multiple words (snake_case)
# full_name

# Multiple words (camelCase)
# fullName


# =====================================
# Escape Characters
# =====================================

print('"Hello World"')

print("\"Hello World\"")

print('\'Hello World\'')


# =====================================
# Carriage Return (\r)
# =====================================

print("hello\r1231")