# While repeats code while condition is True.


##################################################
# Basic While Loop
##################################################

i = 0


while i < 10:

    print(i)

    i += 1



##################################################
# Important Note
##################################################

# Always update the variable inside while loop.
# Otherwise the loop will never stop.



